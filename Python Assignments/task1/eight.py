# If one data type value is assigned to ‘a’ variable and then a different data type value is assigned to ‘a’
# again. Will it change the value? If Yes then Why?


# Yes, assigning a different data type value to a variable that already has a value will change its value and data type.
# Python variables are dynamically typed, which means that the data type of a variable can change at runtime depending on the 
# type of value assigned to it.

a = 10   
print(a)   
print(type(a)) 

a = "hello"  
print(a)     
print(type(a))  



